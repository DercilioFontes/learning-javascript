
// Natural logarithm of x
console.log(Math.log(Math.E));
// 1

console.log(Math.log(17.5));
// 2.8622008809294686

// Base 10 logarithm of x
// Equivalent to Math.log(x)/ Math.log(10)
console.log(Math.log10(10));
// 1

console.log(Math.log10(16.7));
// 1.2227164711475833

// Base 2 logarithm of x 
// Equivalent to Math.log(x)/ Math.log(2)

console.log(Math.log2(2));
// 1

console.log(Math.log2(5));
// 2.321928094887362

// Natural logarithm of 1 + x 
// Equivalent to Math.log(1 + x)
console.log(Math.log1p(Math.E - 1));
// 1

console.log(Math.log1p(17.5));
// 2.917770732084279