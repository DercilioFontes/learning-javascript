
// fundamental constants

// the root of the natural logarithm
console.log(Math.E);
// 2.718281828459045

// the ratio of a circle's circumference to its diameter:
console.log(Math.PI);
// 3.141592653589793

// logarithmic convenience constants -- these can be accessed through library
// calls, but they're commonly used enough to warrant convenience constants

// the natural logarithm of 2:
console.log(Math.LN2);
// 0.6931471805599453

// the natural logarithm of 10:
console.log(Math.LN10);
// 2.302585092994046

// the base 2 logarithm of Math.E:
console.log(Math.LOG2E);
// 1.4426950408889634

// the base 10 logarithm of Math.E:
console.log(Math.LOG10E);
// 0.4342944819032518

// algebraic convenience constants

// the square root of 1/2:
console.log(Math.SQRT1_2);
// 0.7071067811865476

// the square root of 2:
console.log(Math.SQRT2);
// 1.4142135623730951