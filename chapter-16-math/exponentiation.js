
console.log(Math.pow(2, 3));
// 8

console.log(Math.pow(1.7, 2.3));
// 3.388695291147646

// Equivalent to
// Math.pow(x, 0.5)
console.log(Math.sqrt(16));
// 4

console.log(Math.sqrt(15.5));
// 3.9370039370059056

// Cube root of x
// Equivalent to Math.pow(x, 1/3)
console.log(Math.cbrt(27));
// 3

console.log(Math.cbrt(22));
// 2.8020393306553872

// Equivalent to Math.pow(Math.E, x)
console.log(Math.exp(1));
// 2.718281828459045

console.log(Math.exp(5.5));

// Equivalent to Math.exp(x) - 1
console.log(Math.expm1(1));
// 1.718281828459045

console.log(Math.expm1(5.5));
// 243.69193226422038

// Square root of sum of arguments:
console.log(Math.hypot(3, 4));
// 5

console.log(Math.hypot(2, 3, 4));
// 5.385164807134504